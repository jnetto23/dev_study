# DDL - DATA DEFINITION LANGUAGE (Linguagem de Definição de Dados)
# -> Comandos: CREATE, DROP E ALTER (comandos de estrutura)

# DCL - DATA CONTROL LANGUAGE (Linguagem de Controle de Dados)
# -> Comandos: GRANT, REVOKE

# DML - DATA MANIPULATION LANGUAGE (Linguagem de Manipulação de Dados)
# -> Comandos: INSERT, DELETE, UPDATE

# DTL - TRANSACTION CONTROL LANGUAGE (Linguagem de Transação de Dados)
# -> Comandos: START TRANSACTION, COMMIT, SAVEPOINT, ROLLBACK

# DQL - DATA QUERY LANGUAGE (Linguagem de Consulta de Dados)
